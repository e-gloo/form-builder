import { useVueFlow } from '@vue-flow/core'
import { ref, watch, type Ref } from 'vue'
import { useFormBuilder } from './useFormBuilder'

const { addQuestion } = useFormBuilder

function useDragAndDrop() {
  const draggedType: Ref<string|null> = ref(null)
  const isDragOver: Ref<boolean> = ref(false)
  const isDragging: Ref<boolean> =  ref(false)

  const { screenToFlowCoordinate } = useVueFlow('vue-flow-project')

  watch(isDragging, (dragging) => {
    document.body.style.userSelect = dragging ? 'none' : ''
  })

  function onDragStart(event, type) {
    if (event.dataTransfer) {
      event.dataTransfer.setData('application/vueflow', type)
      event.dataTransfer.effectAllowed = 'move'
    }

    draggedType.value = type
    isDragging.value = true

    document.addEventListener('drop', onDragEnd)
  }

  /**
   * Handles the drag over event.
   *
   * @param {DragEvent} event
   */
  function onDragOver(event) {
    event.preventDefault()

    if (draggedType.value) {
      isDragOver.value = true

      if (event.dataTransfer) {
        event.dataTransfer.dropEffect = 'move'
      }
    }
  }

  function onDragLeave() {
    isDragOver.value = false
  }

  function onDragEnd() {
    isDragging.value = false
    isDragOver.value = false
    draggedType.value = null
    document.removeEventListener('drop', onDragEnd)
  }

  /**
   * Handles the drop event.
   *
   * @param {DragEvent} event
   */
  function onDrop(event) {
    const position = screenToFlowCoordinate({
      x: event.clientX,
      y: event.clientY,
    })

    addQuestion(position.x, position.y)
  }

  return {
    draggedType,
    isDragOver,
    isDragging,
    onDragStart,
    onDragLeave,
    onDragOver,
    onDrop,
  }
}
const dnd = useDragAndDrop()

export {
  dnd as useDragAndDrop
}
